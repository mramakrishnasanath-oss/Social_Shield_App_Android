  static String translate(String lang, String key) {
    if (!_localizedValues.containsKey(lang)) {
      return _localizedValues['en']?[key] ?? key;
    }
    return _localizedValues[lang]?[key] ?? _localizedValues['en']?[key] ?? key;
  }
